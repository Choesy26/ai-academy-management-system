DROP TABLE IF EXISTS public.parents CASCADE;
DROP TABLE IF EXISTS public.classes CASCADE;

-- 1. 반(Classes) 테이블 생성
CREATE TABLE public.classes (
  id bigint generated by default as identity primary key,
  name text not null, -- 반 이름 (예: '가우스 반', '오일러 반', '뉴턴 반')
  description text, -- 반 설명
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 2. 학부모(Parents) 테이블 생성
CREATE TABLE public.parents (
  id bigint generated by default as identity primary key,
  name text, -- 학부모 이름 (선택사항)
  phone text, -- 학부모 전화번호 (선택사항)
  relationship text not null default '모', -- 보호자 관계 ('부', '모', '기타 보호자')
  type text not null default '격려형', -- 학부모 성향
  sensitivity text not null default '보통', -- 보고서 민감도
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. 학생(Students) 테이블 생성 (학부모 및 반 테이블을 참조)
CREATE TABLE public.students (
  id bigint generated by default as identity primary key,
  name text not null,
  phone text, -- 학생 전화번호 (선택사항)
  school text,
  grade text,
  goal text,
  parent_id bigint references public.parents(id) on delete set null, -- 학부모 외래키 연결
  class_id bigint references public.classes(id) on delete set null, -- 반 외래키 연결
  "lastReport" text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 4. 분석 기록(Analyses) 테이블 생성 - student_id로 연결
CREATE TABLE public.analyses (
  id bigint generated by default as identity primary key,
  student_id bigint references public.students(id) on delete cascade,
  school text,
  test text,
  date text,
  score integer,
  weakness text,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

